package edunova.FloraFauna;

public abstract class NaturePark {

	private String name;
	private char id;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public char getId() {
		return id;
	}
	public void setId(char id) {
		this.id = id;
	}
	
	
	
	
}
